class casilla {
 int valor;
 int tipo;
 int x, y;

    public casilla() {
        this.valor = valor;
        this.tipo = tipo;
        this.x = x;
        this.y = y;
    }

    

    public int getValor() {
        return valor;
    }

    public void setValor(int valor) {
        this.valor = valor;
    }

    public int getTipo() {
        return tipo;
    }

    public void setTipo(int tipo) {
        this.tipo = tipo;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
 
}
